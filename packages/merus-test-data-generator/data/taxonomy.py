# ---------------------------------------------------------------------------
# Reverse lookup: subtype → parent type
# ---------------------------------------------------------------------------

SUBTYPE_TO_TYPE: dict[str, str] = {}
for _type, _subtypes in DOCUMENT_TYPE_TO_SUBTYPES.items():
    for _subtype in _subtypes:
        SUBTYPE_TO_TYPE[_subtype] = _type


def get_type_for_subtype(subtype: str) -> str | None:
    """Return the parent DocumentType for a given subtype string."""
    return SUBTYPE_TO_TYPE.get(subtype)


def get_subtypes_for_type(doc_type: str) -> list[str]:
    """Return all subtypes for a given parent type."""
    return DOCUMENT_TYPE_TO_SUBTYPES.get(doc_type, [])
