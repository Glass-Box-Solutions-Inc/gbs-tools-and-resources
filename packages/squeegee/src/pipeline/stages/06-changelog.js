/**
 * Stage 06: Per-Project CHANGELOG.md
 *
 * NEW stage — generates changelogs from git commits using conventional commit format.
 * Daily grouping, 90-day retention in file. Manual sections preserved via markers.
 *
 * @Developed & Documented by Glass Box Solutions, Inc. using human ingenuity and modern technology
 */

const fs = require('fs').promises;
const path = require('path');
const { log, fileExists, ensureDir } = require('../utils');
const { resolveProjectPath } = require('../config');
const { updateSections, hasSection } = require('../formatters/sections');
const { timestamp } = require('../formatters/markdown');

// Map conventional commit types to changelog sections
const TYPE_MAP = {
  feat: 'Added',
  fix: 'Fixed',
  refactor: 'Changed',
  perf: 'Changed',
  docs: 'Documentation',
  test: 'Testing',
  chore: 'Maintenance',
  build: 'Maintenance',
  ci: 'Maintenance',
  style: 'Changed',
};

async function run(config, _discovery, gitAnalysis) {
  log('Stage 6: Generating per-project changelogs...', 'info');

  const results = { created: [], updated: [], skipped: [] };

  for (const project of config.projects) {
    const projectPath = resolveProjectPath(config, project.path);
    if (!(await fileExists(projectPath))) continue;

    const gitData = gitAnalysis?.projects?.[project.name];
    if (!gitData || gitData.commits.length === 0) {
      results.skipped.push(project.name);
      continue;
    }

    const changelogPath = path.join(projectPath, 'CHANGELOG.md');

    if (await fileExists(changelogPath)) {
      const updated = await updateChangelog(changelogPath, project, gitData, config);
      if (updated) {
        results.updated.push(project.name);
      } else {
        results.skipped.push(project.name);
      }
    } else {
      await createChangelog(changelogPath, project, gitData, config);
      results.created.push(project.name);
    }
  }

  log(`CHANGELOG — created: ${results.created.length}, updated: ${results.updated.length}`, 'success');
  return results;
}

async function updateChangelog(changelogPath, project, gitData, config) {
  let content = await fs.readFile(changelogPath, 'utf-8');

  const newEntries = formatCommitsAsChangelog(gitData.commits, config);

  if (hasSection(content, 'changelog-entries')) {
    // Merge new entries with existing
    const existingEntries = extractExistingEntries(content);
    const merged = mergeEntries(existingEntries, newEntries);
    const trimmed = trimOldEntries(merged, config.changelog.retentionDays);

    return await updateSections(changelogPath, { 'changelog-entries': trimmed });
  }

  // No markers — inject them
  const markerBlock = `\n<!-- SQUEEGEE:AUTO:START changelog-entries -->\n${newEntries}\n<!-- SQUEEGEE:AUTO:END changelog-entries -->\n`;

  // Insert after the header section
  const firstH2 = content.indexOf('\n## ');
  if (firstH2 > -1) {
    content = content.slice(0, firstH2) + markerBlock + content.slice(firstH2);
  } else {
    content += markerBlock;
  }

  await fs.writeFile(changelogPath, content, 'utf-8');
  return true;
}

async function createChangelog(changelogPath, project, gitData, config) {
  const date = timestamp();
  const entries = formatCommitsAsChangelog(gitData.commits, config);

  const content = `# ${project.name} - Changelog

**Last Updated:** ${date}
**Generated by:** Squeegee

All notable changes to this project are documented here. Entries are auto-generated from git commits.

---

<!-- SQUEEGEE:AUTO:START changelog-entries -->
${entries}
<!-- SQUEEGEE:AUTO:END changelog-entries -->

---

*Auto-generated by Squeegee Documentation System. Manual notes can be added outside the markers.*
`;

  await ensureDir(path.dirname(changelogPath));
  await fs.writeFile(changelogPath, content, 'utf-8');
  log(`Created CHANGELOG.md for ${project.name}`, 'success');
}

/**
 * Group commits by date and format as changelog entries.
 */
function formatCommitsAsChangelog(commits, config) {
  // Group by date
  const byDate = {};
  for (const commit of commits) {
    const date = commit.date.split('T')[0];
    if (!byDate[date]) byDate[date] = [];
    byDate[date].push(commit);
  }

  const lines = [];
  const sortedDates = Object.keys(byDate).sort().reverse();

  for (const date of sortedDates) {
    lines.push(`### ${date}`);
    lines.push('');

    // Group by change type within the day
    const byType = {};
    for (const commit of byDate[date]) {
      const section = TYPE_MAP[commit.type] || 'Other';
      if (!byType[section]) byType[section] = [];
      byType[section].push(commit);
    }

    for (const [section, sectionCommits] of Object.entries(byType)) {
      lines.push(`**${section}:**`);
      for (const commit of sectionCommits) {
        const scope = commit.scope ? `**${commit.scope}:** ` : '';
        lines.push(`- ${scope}${commit.description} (\`${commit.hash.slice(0, 7)}\`)`);
      }
      lines.push('');
    }
  }

  return lines.join('\n').trim();
}

function extractExistingEntries(content) {
  const startMarker = '<!-- SQUEEGEE:AUTO:START changelog-entries -->';
  const endMarker = '<!-- SQUEEGEE:AUTO:END changelog-entries -->';
  const startIdx = content.indexOf(startMarker);
  const endIdx = content.indexOf(endMarker);
  if (startIdx === -1 || endIdx === -1) return '';
  return content.slice(startIdx + startMarker.length, endIdx).trim();
}

function mergeEntries(existing, newEntries) {
  // Simple merge: put new entries first, then existing (dedupe by date headers)
  if (!existing) return newEntries;

  const existingDates = new Set();
  for (const match of existing.matchAll(/^### (\d{4}-\d{2}-\d{2})/gm)) {
    existingDates.add(match[1]);
  }

  // Only add entries for dates not already in existing
  const newLines = newEntries.split('\n');
  const filteredNew = [];
  let skipSection = false;

  for (const line of newLines) {
    const dateMatch = line.match(/^### (\d{4}-\d{2}-\d{2})/);
    if (dateMatch) {
      skipSection = existingDates.has(dateMatch[1]);
    }
    if (!skipSection) {
      filteredNew.push(line);
    }
  }

  const merged = filteredNew.join('\n').trim();
  return merged ? merged + '\n\n' + existing : existing;
}

function trimOldEntries(content, retentionDays) {
  const cutoff = new Date();
  cutoff.setDate(cutoff.getDate() - retentionDays);
  const cutoffStr = cutoff.toISOString().split('T')[0];

  const lines = content.split('\n');
  const result = [];
  let include = true;

  for (const line of lines) {
    const dateMatch = line.match(/^### (\d{4}-\d{2}-\d{2})/);
    if (dateMatch) {
      include = dateMatch[1] >= cutoffStr;
    }
    if (include) {
      result.push(line);
    }
  }

  return result.join('\n').trim();
}

module.exports = { run };
